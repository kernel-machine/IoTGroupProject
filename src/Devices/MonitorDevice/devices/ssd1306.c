static void initArray(ssd1306_t *ssd1306) {
    for (int i = 0; i < MAX_QUEUE_ELEMENTS; i++) {
        memset(ssd1306->directionArray[i].text, 0, MAX_PLATE_TEXT);
        ssd1306->directionArray[i].arrowDirection = 0;
        ssd1306->directionArray[i].currentStartPosition = 0;
        ssd1306->directionArray[i].enabled = 0;
    }
}

static char *getIndicationString(arrowDirection_e directionE) {
    switch (directionE) {
        case LEFT:
            return "LEFT";
        case RIGHT:
            return "RIGHT";
        case TOP:
            return "TOP";
        case BOTTOM:
            return "BOTTOM";
        case TOP_LEFT:
            return "TOP LEFT";
        case TOP_RIGHT:
            return "TOP RIGHT";
        case BOTTOM_LEFT:
            return "BOTTOM LEFT";
        case BOTTOM_RIGHT:
            return "BOTTOM RIGHT";
        case TURN_AROUND:
            return "TURN AROUND";
        case NO_RESERVATION:
            return "NO_RESERVATION";
        default:
            return "";
    }
}

static size_t countArrayElements(ssd1306_t *ssd1306) {
    size_t size = 0;
    for (int i = 0; i < MAX_QUEUE_ELEMENTS; i++) {
        if (ssd1306->directionArray[i].enabled)
            size++;
    }
    return size;
}

int getStartingLinePixel(int linesNumber) {
    if (linesNumber == 1)
        return 40;
    else if (linesNumber == 2) {
        return 30;
    }
    else
        return 20;
}

int getStartPixelColumn(int textLength, int currentStartPosition) {
    int startPixelColumn =
            textLength > MAX_CHAR_FOR_LINE ? 0 : (MAX_CHAR_FOR_LINE - textLength) *
                                                 (CHAR_WIDTH / 2);

    if (textLength > MAX_CHAR_FOR_LINE)
        startPixelColumn -= currentStartPosition;//ssd1306->directionArray[i].currentStartPosition;

    return startPixelColumn;
}

void *ssd1306_writeArrowThread(void *arg) {

    ssd1306_t *ssd1306 = (ssd1306_t *) arg;

    u8g2_t *u8g2 = &(ssd1306->u8g2);
    u8g2_Setup_ssd1306_i2c_128x64_noname_1(u8g2, U8G2_R0, u8x8_byte_hw_i2c_riotos, u8x8_gpio_and_delay_riotos);
    u8x8_riotos_t user_data =
            {
                    .device_index = I2C_DEV(0),
                    .pin_cs = GPIO_UNDEF,
                    .pin_dc = GPIO_UNDEF,
                    .pin_reset = GPIO_UNDEF,
            };

    u8g2_SetUserPtr(u8g2, &user_data);
    u8g2_SetI2CAddress(u8g2, ssd1306->i2cAddr);

    u8g2_InitDisplay(u8g2);
    u8g2_SetPowerSave(u8g2, 0);

    while (1) {
        if (countArrayElements(ssd1306) == 0) {
            u8g2_SetPowerSave(u8g2, 1);
        }
        else {
            for (int i = 0; i < MAX_QUEUE_ELEMENTS; i++) {

                if (ssd1306->directionArray[i].enabled) {
                    u8g2_SetPowerSave(u8g2, 0);
                    time_t start_time = xtimer_now_usec();

                    while (xtimer_now_usec() - start_time < MS2US(INTERVAL_PER_PAGE_MS)) {
                        //Cycle of text animation
                        u8g2_FirstPage(u8g2);

                        do {
                            u8g2_SetDrawColor(u8g2, 1);
                            u8g2_SetFont(u8g2, u8g2_font_t0_16b_te);

                            switch (ssd1306->directionArray[i].arrowDirection) {
                                case LEFT:
                                    u8g2_DrawXBMP(u8g2, 0, 0, draw_width, draw_height, left_bits);
                                    break;
                                case RIGHT:
                                    u8g2_DrawXBMP(u8g2, 0, 0, draw_width, draw_height, right_bits);
                                    break;
                                case TOP:
                                    u8g2_DrawXBMP(u8g2, 0, 0, draw_width, draw_height, top_bits);
                                    break;
                                case BOTTOM:
                                    u8g2_DrawXBMP(u8g2, 0, 0, draw_width, draw_height, bottom_bits);
                                    break;
                                case TOP_LEFT:
                                    u8g2_DrawXBMP(u8g2, 0, 0, draw_width, draw_height, left_top_bits);
                                    break;
                                case TOP_RIGHT:
                                    u8g2_DrawXBMP(u8g2, 0, 0, draw_width, draw_height, right_top_bits);
                                    break;
                                case BOTTOM_LEFT:
                                    u8g2_DrawXBMP(u8g2, 0, 0, draw_width, draw_height, left_bottom_bits);
                                    break;
                                case BOTTOM_RIGHT:
                                    u8g2_DrawXBMP(u8g2, 0, 0, draw_width, draw_height, right_bottom_bits);
                                    break;
                                case TURN_AROUND:
                                    u8g2_DrawXBMP(u8g2, 0, 0, draw_width, draw_height, turnaroud_bits);
                                    break;
                                case NO_RESERVATION: {
                                    char *noReservationText = "NO RESERVATION";
                                    int startPixelColumn = getStartPixelColumn(strlen(noReservationText),
                                                                               ssd1306->directionArray[i].currentStartPosition);
                                    u8g2_DrawStr(u8g2, startPixelColumn, 20, noReservationText);
                                    char *bookOnTheWebsite = "BOOK ON THE WEBSITE";
                                    startPixelColumn = getStartPixelColumn(strlen(bookOnTheWebsite),
                                                                           ssd1306->directionArray[i].currentStartPosition);
                                    u8g2_DrawStr(u8g2, startPixelColumn, 40, bookOnTheWebsite);
                                }
                                    break;
                                default:
                                    break;
                            }

                            char *text = ssd1306->directionArray[i].text;
                            const int textLength = strlen(text);
                            int startPixelColumn =
                                    getStartPixelColumn(textLength, ssd1306->directionArray[i].currentStartPosition);

                            u8g2_DrawStr(u8g2, startPixelColumn, SCREEN_HEIGHT, text);

                        } while (u8g2_NextPage(u8g2));

                        ssd1306->directionArray[i].currentStartPosition += TEXT_SPEED;
                        xtimer_msleep(HZ2MS(SCREEN_REFRESH_RATE_HZ));
                    }

                    if (ssd1306->directionArray[i].arrowDirection == NO_RESERVATION
                        && (US2S(xtimer_now_usec()) - ssd1306->directionArray[i].addedAtS) > NO_RESERVATION_TIMEOUT_S) {
                        printf("Signage for %s removed\n", ssd1306->directionArray[i].text);
                        memset(ssd1306->directionArray[i].text, 0, MAX_PLATE_TEXT);
                        ssd1306->directionArray[i].enabled = 0;
                        ssd1306->directionArray[i].arrowDirection = OFF;
                        ssd1306->directionArray[i].currentStartPosition = 0;
                    }
                }
            }
        }
        xtimer_usleep(100); //Otherwise the main thread is never executed
    }
    return NULL;
}

void ssd1306_init(ssd1306_t *ssd1306, uint8_t i2cAddr) {
    ssd1306->i2cAddr = i2cAddr;
    initArray(ssd1306);
    thread_create(rcv_thread_stack, sizeof(rcv_thread_stack),
                  THREAD_PRIORITY_MAIN - 1, THREAD_CREATE_STACKTEST,
                  ssd1306_writeArrowThread, ssd1306, "monitor_thread");
}

void ssd1306_addIndication(ssd1306_t *ssd1306, arrowDirection_e direction, char *plate) {
    for (int i = 0; i < MAX_QUEUE_ELEMENTS; i++) {
        if (ssd1306->directionArray[i].enabled &&
            strcmp(ssd1306->directionArray[i].text, plate) == 0) //Check for dupliacte
            return;
    }

    for (int i = 0; i < MAX_QUEUE_ELEMENTS; i++) {
        if (!ssd1306->directionArray[i].enabled) {
            strcpy(ssd1306->directionArray[i].text, plate);
            ssd1306->directionArray[i].arrowDirection = direction;
            ssd1306->directionArray[i].enabled = 1;
            ssd1306->directionArray[i].addedAtS = US2S(xtimer_now_usec());
            printf("Signage %s for %s added\n", getIndicationString(direction), plate);
            return;
        }
    }
    printf("Cannot add Signage for the plate %s\n", plate);
}

void ssd1306_removeIndication(ssd1306_t *ssd1306, char *plate) {
    for (int i = 0; i < MAX_QUEUE_ELEMENTS; i++) {
        if (strcmp(ssd1306->directionArray[i].text, plate) == 0) {
            memset(ssd1306->directionArray[i].text, 0, MAX_PLATE_TEXT);
            ssd1306->directionArray[i].enabled = 0;
            ssd1306->directionArray[i].arrowDirection = OFF;
            ssd1306->directionArray[i].currentStartPosition = 0;
            printf("Signage for %s removed\n", plate);
        }
    }

}



